/**
 * Get translation for a key in the current language
 */
export function t(key: string, language: 'en' | 'te' = 'en'): string {
  // Support nested keys like 'encouragement.excellent'
  const keys = key.split('.');
  let value: any = translations;

  for (const k of keys) {
    value = value?.[k];
    if (!value) return key; // Return key if not found
  }

  return value[language] || value.en || key;
}

/**
 * Get random encouragement message
 */
export function getRandomEncouragement(language: 'en' | 'te' = 'en'): string {
  const messages = Object.values(translations.encouragement);
  const random = messages[Math.floor(Math.random() * messages.length)];
  return random[language];
}

/**
 * Get time-based greeting
 */
export function getGreeting(language: 'en' | 'te' = 'en'): string {
  const hour = new Date().getHours();
  if (hour < 12) return translations.greetings.morning[language];
  if (hour < 17) return translations.greetings.afternoon[language];
  return translations.greetings.evening[language];
}
